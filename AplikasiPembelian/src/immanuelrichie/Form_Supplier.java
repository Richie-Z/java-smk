    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
     if (txtKode.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Kode Supplier tidak boleh kosong");
            txtKode.requestFocus();
             } 
        else if (txtNama.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nama Supplier tidak boleh kosong");
            txtNama.requestFocus();
        }
        else if (txtKontak.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Kontak tidak boleh kosong");
            txtKontak.requestFocus();
        }
        else if (txtTelp.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Telepon tidak boleh kosong");
            txtTelp.requestFocus();
        }
        else if (txtFax.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Fax tidak boleh kosong");
            txtFax.requestFocus();
        }else if (txtAlamat.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Alamat tidak boleh kosong");
            txtAlamat.requestFocus();
        }else
            try {
                Connection koneksi;
                koneksi = DriverManager.getConnection("jdbc:mysql://localhost/aplikasipembelian","root", "");
                koneksi.createStatement().executeUpdate("insert into tabelsupplier values('"+txtKode.getText()+"','"+txtNama.getText()+"','"+txtKontak.getText()+"','"+txtTelp.getText()+"','"+txtFax.getText()+"','"+txtAlamat.getText()+"')");
                JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan");
                txtKode.setText(null);
                txtNama.setText(null);
                txtKontak.setText(null);
                txtTelp.setText(null);
                txtFax.setText(null);
                txtAlamat.setText(null);
                txtKode.requestFocus();
                tampil(); //memanggil prosedur dengan nama tampil, prosedur harus dibuat terlebih dahulu

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Database Error");
            }
    }//GEN-LAST:event_btnSimpanActionPerformed

