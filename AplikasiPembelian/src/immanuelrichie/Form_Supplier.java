private void tampil(){
    try {
        int row=jTable1.getRowCount();
        for(int i=0; i<row;i++){
            table.removeRow(0);
        }
        Connection koneksi =DriverManager.getConnection("jdbc:mysql://localhost/aplikasipembelian","root", "");
        ResultSet dat=koneksi.createStatement().executeQuery("select * from tabelsupplier");
        while(dat.next()){
            String[] data={dat.getString(1),dat.getString(2),dat.getString(3),dat.getString(4),dat.getString(5),dat.getString(6)};
            table.addRow(data);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane,"Database Error");
    }

}

