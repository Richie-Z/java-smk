    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        try {
            Connection koneksi;
            koneksi = DriverManager.getConnection("jdbc:mysql://localhost/aplikasipembelian","root", "");
            String search = txtCari.getText();
            ResultSet dat = koneksi.createStatement().executeQuery("SELECT * FROM tabelsupplier WHERE supplier like '%"+search+"%' OR kodesup like '%"+search+"%'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            dtm.setRowCount(0);
            String [] data = new String[6];
            int i = 1;
            while(dat.next())
            {
                data[0] = dat.getString("kodesup");
                data[1] = dat.getString("supplier");
                data[2] = dat.getString("kontak");
                data[3] = dat.getString("telepon");
                data[4] = dat.getString("fax");
                data[5] = dat.getString("alamat");
                dtm.addRow(data);
                i++;
            }
            dat.close();
            //txtCari.setText("");
        }
        catch(Exception ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }
    }//GEN-LAST:event_btnCariActionPerformed

