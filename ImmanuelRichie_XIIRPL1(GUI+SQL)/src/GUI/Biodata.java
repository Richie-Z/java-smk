    private void tblbiodataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbiodataMouseClicked
        int baris = tblbiodata.rowAtPoint(evt.getPoint());
        String nik = tblbiodata.getValueAt(baris, 1).toString();
        txtNIK.setText(nik);
        
        String nama = tblbiodata.getValueAt(baris, 2).toString();
        txtNama.setText(nama);
        
        String jrs = tblbiodata.getValueAt(baris, 3).toString();
        cmbJurusan.setSelectedItem(jrs);
        
        String alamat = tblbiodata.getValueAt(baris, 4).toString();
        txtAlamat.setText(alamat);
        
        String email = tblbiodata.getValueAt(baris, 5).toString();
        txtEmail.setText(email);
        
        String telp = tblbiodata.getValueAt(baris, 6).toString();
        txtTelp.setText(telp);
    }//GEN-LAST:event_tblbiodataMouseClicked

