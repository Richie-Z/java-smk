    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(txtNomor.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Nomor Transaksi tidak boleh kosong");
            txtNomor.requestFocus();
        }else if(txtTanggal.getDate() == null){
            JOptionPane.showMessageDialog(null,"Tanggal tidak boleh kosong");
            txtTanggal.requestFocus();
        }else if(txtKodeSup.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Kode Supplier tidak boleh kosong");
            txtKodeSup.requestFocus();
        }else{
            try{
                Connection koneksi;
                koneksi = DriverManager.getConnection("jdbc:mysql://localhost/aplikasipembelian","root", "");
                Date date = txtTanggal.getDate();
                SimpleDateFormat ft = new SimpleDateFormat("YYYY-MM-dd");
                koneksi.createStatement().executeUpdate("insert into tabelpembelian values('"+txtNomor.getText()+"','"+ft.format(date)+"','"+txtKodeSup.getText()+"')");
                JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan");
                txtNomor.setText(null);
                txtTanggal.setDate(null);
                txtKodeSup.setText(null);
          
            }catch(SQLException ex){
                 JOptionPane.showMessageDialog(rootPane, "Database Error:"+ex);
                 System.err.println("error : " +ex);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

