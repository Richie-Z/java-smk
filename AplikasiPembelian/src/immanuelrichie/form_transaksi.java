private void tampil(){
    try {
        int row=jTable1.getRowCount();
        for(int i=0; i<row;i++){
            table.removeRow(0);
        }
        Connection koneksi =DriverManager.getConnection("jdbc:mysql://localhost/aplikasipembelian","root", "");
        ResultSet dat=koneksi.createStatement().executeQuery("SELECT tb.namabarang, tb.hargabeli,tbd.jumlah, tbd.totalharga FROM tabelpembeliandetail tbd INNER JOIN tabelbarang tb ON tb.kodebarang = tbd.kodebarang");
        int sum=0;
        while(dat.next()){
            String[] data={dat.getString(1),dat.getString(2),dat.getString(3),dat.getString(4)};
            table.addRow(data);
            String total = dat.getString("totalharga");  
            int qt = parseInt(total);
            sum+=qt;
            String sumS = Integer.toString(sum) ;
            txtGrandTotal.setText(sumS);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane,"Database Error");
         System.err.println("error (search) : " +ex);
    }

}    
