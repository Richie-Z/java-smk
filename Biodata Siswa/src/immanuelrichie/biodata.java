    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
         try {
            stat.executeUpdate("INSERT INTO siswa VALUES ('"+txtNama.getText()+"','"+txtNis.getText()+"','"
                    +cmbKelas.getSelectedItem()+"','"+cmbJurusan.getSelectedItem()+"')");      
            clear(); 
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan Data"); 
        }
        catch (SQLException | HeadlessException e) { 
            JOptionPane.showMessageDialog(null, "Perintah Salah : "+e);
        }finally{
         tabel();
        } 
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
         try {
            stat.executeUpdate("UPDATE siswa SET "
                + "nama='"+txtNama.getText()+"',"
                + "nis='"+txtNis.getText()+"',"
                + "kelas='"+cmbKelas.getSelectedItem()+"',"
                + "jurusan='"+cmbJurusan.getSelectedItem()+"'"
                + " WHERE nis='"+txtNis.getText()+"'");
            clear(); 
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan Data"); 
        }
        catch (SQLException | HeadlessException e) { 
            JOptionPane.showMessageDialog(null, "Perintah Salah : "+e);
        }finally{
             tabel();
        } 
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try { 
            String sql="DELETE FROM siswa WHERE nis='"+txtNis.getText()+"'" ;
            Statement stm = con.createStatement();
            stm.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Berhasil");  
            this.setVisible(true);
            setVisible(true);
        }catch (Exception e) { 
            JOptionPane.showMessageDialog(this, "pesan salah : "+e);
        }finally{
            clear();
            tabel();
        }   
    }//GEN-LAST:event_btnHapusActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris = jTable1.getSelectedRow();
        String nis = jTable1.getValueAt(baris, 0).toString();
        txtNis.setText(nis);
        
        String nama = jTable1.getValueAt(baris, 1).toString();
        txtNama.setText(nama);
        
        String kelas = jTable1.getValueAt(baris, 2).toString();
        cmbKelas.setSelectedItem(kelas);
        
        String jrs = jTable1.getValueAt(baris, 3).toString();
        cmbJurusan.setSelectedItem(jrs);
    }//GEN-LAST:event_jTable1MouseClicked

