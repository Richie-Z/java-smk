    private void tabel(){ 
        DefaultTableModel t= new DefaultTableModel();
        t.addColumn("NIS"); 
        t.addColumn("NAMA SISWA"); 
        t.addColumn("KELAS");
        t.addColumn("KOMPETENSI KEAHLIAN");
        jTable1.setModel(t); 
        try{ 
           res=stat.executeQuery("SELECT * FROM siswa"); 
           while (res.next()) { 
               t.addRow(new Object[]{ 
                   res.getString("nis"),
                   res.getString("nama"), 
                   res.getString("kelas"), 
                   res.getString("jurusan"),
               }); 
           }
        }catch (Exception e) { 
           JOptionPane.showMessageDialog(null,"gagal:"+e); 
        }
    }
